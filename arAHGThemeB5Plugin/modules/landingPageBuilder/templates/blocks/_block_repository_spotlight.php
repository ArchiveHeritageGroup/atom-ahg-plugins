<?php
/**
 * Repository Spotlight Block Template
 */
$repo = $data ?? null;
$showLogo = $config['show_logo'] ?? true;
$showDescription = $config['show_description'] ?? true;
$showContact = $config['show_contact'] ?? false;
$showHoldingsCount = $config['show_holdings_count'] ?? true;
?>

<?php if (empty($repo)): ?>
  <p class="text-muted">No repository selected.</p>
<?php else: ?>
  <div class="repository-spotlight">
    <div class="row align-items-center">
      <?php if ($showLogo): ?>
        <div class="col-md-3 text-center mb-3 mb-md-0">
          <div class="bg-light rounded p-3">
            <i class="bi bi-building display-3 text-primary"></i>
          </div>
        </div>
      <?php endif ?>
      
      <div class="<?php echo $showLogo ? 'col-md-9' : 'col-12' ?>">
        <h3 class="mb-2">
          <a href="<?php echo url_for(['module' => 'repository', 'slug' => $repo['slug']]) ?>" 
             class="text-decoration-none">
            <?php echo esc_entities($repo['name'] ?? '') ?>
          </a>
        </h3>
        
        <?php if ($showContact && (!empty($repo['city']) || !empty($repo['region']))): ?>
          <p class="text-muted mb-2">
            <i class="bi bi-geo-alt"></i>
            <?php echo esc_entities(implode(', ', array_filter([$repo['city'], $repo['region']]))) ?>
          </p>
        <?php endif ?>
        
        <?php if ($showDescription && !empty($repo['description'])): ?>
          <p class="mb-3"><?php echo truncate_text(strip_tags($repo['description']), 200) ?></p>
        <?php endif ?>
        
        <div class="d-flex flex-wrap gap-3">
          <?php if ($showHoldingsCount && isset($repo['holdings_count'])): ?>
            <span class="badge bg-primary fs-6">
              <i class="bi bi-archive"></i> <?php echo number_format($repo['holdings_count']) ?> Holdings
            </span>
          <?php endif ?>
          
          <a href="<?php echo url_for(['module' => 'repository', 'slug' => $repo['slug']]) ?>" 
             class="btn btn-outline-primary btn-sm">
            View Repository <i class="bi bi-arrow-right"></i>
          </a>
        </div>
        
        <?php if (!empty($repo['sample_holdings'])): ?>
          <div class="mt-3">
            <h6 class="small text-muted">Sample Holdings:</h6>
            <ul class="list-unstyled small">
              <?php foreach ($repo['sample_holdings'] as $holding): ?>
                <li>
                  <a href="<?php echo url_for(['module' => 'informationobject', 'slug' => $holding->slug]) ?>" 
                     class="text-decoration-none">
                    <?php echo esc_entities($holding->title ?? $holding->slug) ?>
                  </a>
                </li>
              <?php endforeach ?>
            </ul>
          </div>
        <?php endif ?>
      </div>
    </div>
  </div>
<?php endif ?>
